import 'package:emovied/main_export.dart';

class CustomTextStyle {
  static TextStyle header() {
    return const TextStyle(color: Colors.white, fontSize: 22, fontWeight: FontWeight.bold);
  }

  static TextStyle title() {
    return const TextStyle(color: Colors.white, fontSize: 18, fontWeight: FontWeight.bold);
  }

  static TextStyle title2() {
    return const TextStyle(color: Colors.white, fontSize: 14, fontWeight: FontWeight.bold);
  }

  static TextStyle subtitle() {
    return const TextStyle(color: Colors.white, fontSize: 14);
  }

  static TextStyle widgetTitle() {
    return const TextStyle(color: Colors.blue, fontSize: 22, fontWeight: FontWeight.bold);
  }
}
